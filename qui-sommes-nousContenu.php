 <h2 id="wiart"><br />Jean-Pierre Wiart, chef d’orchestre</h2>
<img src="photos/jean_pierre_wiart.jpg" width="100" height="120" class="imgl borderedbox inspace-5" />
<P>Après avoir travaillé sous la baguette des plus grands chefs tels que Lorin Maazel, Riccardo Mutti, Georges Prêtre, Charles Dutoit ou encore Daniele Gatti, Jean-Pierre Wiart début sa carrière de chef sur les conseils de son ami et professeur Stéphane Cardon, chef associé de Michel Plasson à l’Orchestre National du capitole de Toulouse.
Depuis, il a eu sous sa direction plusieurs artistes de renommée internationale et aborde un répertoire large et éclectique de la musique classique.<br />
Appelé par diverses formations à travers la France, il est à la tête de l’Orchestre philharmonique des Hauts de France-Cambrai.<br />
Ses prestations remarquées, furent notamment saluées lors des concerts lyriques qu’il affectionne tout particulièrement.</P>

<h2 id="adrien"><br />Adrien Jourdain, Metteur en scène</h2>
<img src="photos/adrienjourdain.jpg" height="120" class="imgl borderedbox inspace-5" />
<p>Adrien Jourdain découvre et tombe amoureux du théâtre à 7 ans. Pendant 15 ans, il participera à de nombreux spectacles amateurs. Le dernier en date : Le Lion en Hiver de James Goldman est sélectionné parmi les trois meilleurs spectacles d’Ile de France lors du Masque d’Or 2011. Parallèlement, Adrien suit des études de cinéma à l’Eicar. Il recevra le prix du Meilleur court-métrage de fin d’études des mains de Dominique Pinon. Son dernier court-métrage rallie ses deux passions : il est adapté d’un dialogue théâtral de Xavier Durringer. Après deux ans passés à assister les réalisateurs de la chaîne de télévision Public Sénat, Adrien Jourdain revient à ses premières amours et commence l'assistanat mise en scène sur des opéras. En six ans, il participe au montage de plusieurs spectacles avec différentes compagnies. Son histoire avec l'Opéra commence avec La Créole d'Offenbach monté par la compagnie des Tréteaux Lyriques. Il travaille ensuite avec la compagnie Opéra Côté Chœur. Ensemble, ils montent Carmen de Bizet, Le Barbier de Séville de Rossini, La Traviata de Verdi et de nombreux autres opéras. Il suivra les tournées de ces spectacles qui se joueront dans des lieux variés tels que l'Espace Cardin à Paris, le Pin Galant à Mérignac, le Beffroi de Montrouge, etc. N'oubliant pas son envie de cinéma, Adrien est engagé comme assistant réalisateur sur des longs-métrages tel que <i>Cessez le feu</i> d'Emmanuel Courcol avec Romain Duris ou <i>Alice</i> de Joséphine Mackerras. En 2020, il met en scène sa première opérette : La Grande Duchesse de Gerolstein d’Offenbach au théâtre du Gymnase.</p>
<p>Il mettra prochainement en scène Carmen de Bizet et La flûte enchantée de Mozart.</p>

