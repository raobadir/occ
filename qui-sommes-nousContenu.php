        	<h6 class="heading font-x3" >Qui sommes-nous</h6><br/>
				<div class="centre"><iframe width="560" height="315" src="https://www.youtube.com/embed/f7IfqKQaD7Y" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
						<p class="byline">Vidéo créée en 2019</p>
				</div>		
					<p><cite>Opéra Côté Chœur</cite> produit et diffuse depuis 10 ans en Ile-de-France - et maintenant au-delà - des opéras à des prix raisonnables afin d’aller à la rencontre de publics nouveaux.<br />Elle propose des œuvres du <strong>répertoire</strong>, des œuvres tournées vers le <strong>jeune public</strong>, et envisage prochainement la <strong>création</strong> d’une œuvre contemporaine.</p>
<p>Notre compagnie propose un vrai travail de mise en scène sur les œuvres qu’elle présente. Elle ambitionne une grande qualité non seulement musicale mais aussi esthétique.</p>
<p>Notre compagnie s’est fixé comme objectif de rendre l’opéra accessible, voire familier aux enfants et de rompre avec l’image d’un art élitiste.</p>	
<p>Elle accompagne par conséquent, à la demande, les spectacles lyriques proposés par la compagnie, d’une action pédagogique destinée au très jeune public. Cette initiation comprend la découverte et l’explicitation des codes et conventions qui sous-tendent ce type de spectacle, afin de familiariser l’enfant avec un univers susceptible de lui procurer des émotions artistiques immédiates, émotions qu’il pourra approfondir par la suite au gré de ses diverses expériences personnelles.<br /><br /></p>				
