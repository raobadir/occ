        <h2><br />Isabelle Huchet, Scénographe</h2>
					<img src="photos/LBH-037.jpg" alt="Isabelle Huchet" width="100" height="123"  class="imgl borderedbox inspace-5" />
					<p>Tout mon CV tient dans mon plaisir de faire mon métier. Près de quarante ans que ça dure. L'école (l'ENSATT) est bien loin. C'était à la fin des années 70. En en sortant, il y a eu 10 ans de théâtre comme on en décrit dans les romans noirs. Du théâtre sans le sou, sans lieu, mais pas sans talents.<br />Lors de rétrospectives, j'ai pu exposer mes tout premiers costumes, tissés de mes petites mains d'apprentie, pour <strong>Macbeth</strong>. C’est un beau spectacle, mais quelle époque difficile ! J’ai été bien soulagée d'entrer, par hasard, dans le monde de l'événementiel. C'était avant la guerre du Golfe, les entreprises étaient riches, les projets déments. Pendant cette période, j'ai beaucoup appris, en concevant des décors volumineux pour de grandes et belles scènes.<br />
Mais le théâtre, les beaux textes me manquaient. Aussi, à la première occasion, je suis revenue au spectacle. C'était sur un opéra pour et avec des enfants, grandiose, avec cette école de chant incroyable qu'est le <strong>Créa</strong>, à Aulnay sous bois. Ils montaient <strong>le Tour du monde en 80 jours</strong>, musique de Louis Dunoyer de Segonzac, mise en scène de Patrick Paroux. 270 costumes, 20 changements de décors conçus par mon amie Claire Belloc. Une somptuosité jouée cinq fois !<br />
Depuis, je me suis habituée aux opéras qu'on monte en plusieurs mois et qui meurent après trois jours. Tout le bonheur est dans la préparation, la gestation. Après, c'est l'arrachement.<br />Aux créations, toujours intimidantes, mais toujours passionnantes, pour la réunion des opéras de province, tels que, en 2009,  <strong>Les Orages désirés</strong>, musique de Gérard Condé et <strong>Ik et Ox</strong>, musique d'André Bon, mis en scène par Christine Marest, se mêlent les œuvres de répertoire : <strong>Carmen, Hamlet, Orphée et Eurydice, Paillasse, La Tosca</strong>, des opérettes d'Offenbach, le tout mis en scène et dirigé par Samuel Sené.<br />Dernière étape, avec mon mari Bernard Jourdain et ma sœur, nous avons donné naissance à Opéra Côté Chœur qui absorbe une bonne partie de mon emploi du temps, même si je continue de travailler avec mes fidèles amis.<br /></p>
		

