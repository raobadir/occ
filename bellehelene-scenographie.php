						<p><br />La Belle Hélène d'Adrien Jourdain se déroule dans un lieu unique, un plateau de télévision. Nous
savons tous, sauf l'héroïne, qu'il s'agit d'un lieu clos, entouré d'un autre monde auquel elle n'a pas accès.
Ce monde dans lequel elle vit depuis toujours est odieusement propre, faussement idéal, pratique, rigide mais
coloré, en dépit d'une dominante de blanc-bleu de carte postale.</p><p>
Des différences de niveau permettent aux initiés de vivre dans le réel à l'insu de notre naïve Hélène. L'écran
sur lequel se déroule l'émission retransmise est invisible pour elle. Le dispositif que nous avons mis au point
offre de nombreuses possibilités de jeu tout en conservant les poncifs de ce type d'émission. Les objets et
le mobilier colorés, design, too much, ajoutent à l'ambiance bling bling mais aussi à la bonne humeur de
notre opérette.</p><p>
Pour les costumes, on vise l'élégance des formes. L'inspiration est Haute-Couture, mais les matières appartiennent d'avantage au monde du showbiz
: paillette, fourrure, cuir, perles, plumes, le tout dans un camaïeu d'indigo et de blanc.
Les costumes seront inspirés de la mode actuelle, celle qui parade au pied du palais du Festival de
Cannes, mais toute incursion dans un siècle depuis longtemps dépassé est envisageable. Pas de
limite à l'imagination, de la Pompadour à Lady Gaga.</p>
<p>Sur la rigueur structurelle du décor éclateront l'audace des costumes, le délire des perruques, la
licence du mobilier.</p><p>
Une folie maîtrisée par Vénus dans le but de faire rêver, et par Adrien Jourdain, dans le but de
s'amuser.</p>
