        <h1 class="heading font-x3" >Tosca</h1>
	<h2>un opéra de Giacomo Puccini</h2>
						<img src='photos/fabienne_tosca.jpg' alt="" class="imgr borderedbox inspace-10"/>
						<p><br />Le livret de Tosca est écrit comme une tragédie resserrée, autour de trois personnages principaux. L’action commence l’après-midi du premier jour et finit à l’aube du lendemain. Le rythme est haletant, sans répit dans la composition.<br />
						         Le sujet est banal : un couple d’artistes amoureux l’un de l’autre, se trouve confronté au cynisme et à la vénalité d’un homme de pouvoir dans un climat politique proche de la terreur. Et pourtant la musique et le livret élèvent cette histoire au rang d’une tragédie classique.</p><p> 
Le quotidien de Tosca, avant que Scarpia ne jette son dévolu sur elle, consiste à jouer des personnages, à mettre en scène son existence plutôt qu’à la vivre, comme Mario baignant dans un monde sublimé en peignant ses fresques.<br />« Ce sont de doux rêveurs », dirait le commun des mortels.<br />Scarpia va crever la bulle dans laquelle ils évoluent, et offrir à l’actrice son plus beau rôle, en la confrontant à un dilemme cornélien : se donner à Scarpia pour sauver Mario, ou signer l’arrêt de mort de son amant en ne cédant pas.<br /><br /></p>											

