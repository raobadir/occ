	 <div class="two_third first"><h1 class="heading font-x3" >Samson et Dalila</h1>   
         <h3>Opéra en trois actes de Camille Saint-Saëns</h3>  
<p>Prisonniers des Philistins, les Hébreux
implorent le Dieu d’Israël. Samson, héros
valeureux, tue le satrape Abimélech et
encourage les siens à se rebeller contre
les Philistins. Le peuple d’Israël rompt
alors ses chaines et parvient à s’enfuir
sous les menaces vipérines du Grand
Prêtre de Dagon qui jure vengeance.<br />
Celui-ci retrouve la voluptueuse philistine,
Dalila, décidée à séduire Samson, de
façon à percer le secret de sa force, dans
le seul but de venger son peuple. Dans un
duo débordant de lyrisme, Dalila fait
chavirer le coeur du héros qui, malgré les
mises en garde d’un vieillard hébreu, succombe
à son amour...et au piège tendu par la
belle.</p>
<p>La musique de Saint-Saëns se situe à la charnière entre la tradition française du grand opéra et l’esprit
wagnérien qui touche nombre de compositeurs du XIXe siècle. Le rôle de Samson est tenu par un
ténor (il incarne l’héroïsme de la fonction du personnage), tandis que la suave Dalila est jouée par une
mezzo-soprano ce qui confère au personnage le caractère fatal qui lui convient. L’importance des chœurs est la trace du projet initial de Saint-Saëns, qui pensait composer un oratorio sur le sujet.</p>             
