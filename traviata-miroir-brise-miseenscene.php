<p>Au début de l’opéra, Violetta rencontre Alfredo Germont. L’amour passionné qu’Alfredo montre à la Traviata, va déclencher chez elle une révolution intérieure : elle qui s’enivrait de plaisirs, réalise que son existence, ne vaut rien ! Qu’un regard d’amour sincère brise net ses certitudes.<br />Au 2ème acte,  une seconde rencontre, avec le père d’Alfredo, va mettre à nu une blessurere foulée : l’absence du père.<br />L’opéra de Verdi se présente donc comme un parcours initiatique pendant lequel l’héroïne fait deux découvertes majeures : celle de l’amour passion et celle de l’amour filial. Elle subit deux épreuves qui vont l’amener au seuil de la mort.<br />Les seize premières mesures de l’opéra nous disent avec évidence que tout est consommé, et que les trois premiers actes ne sont qu’un ultime regard jeté par Violetta sur son passé. Pendant cette ouverture, le spectateur la découvre allongée, mourante, attendant désespérément le retour de son amant en relisant ses lettres. Des images de sa vie passée affluent, le premier acte peut commencer.
