					<p>Après des études aux Beaux-arts d’Orléans, Antoine Milian s’engage sur la double voie de scénographe et de plasticien. Son travail personnel de créateur lui donne accès à divers manifestations artistiques où il réalise des installations in-situ, des expositions personnelles ou collectives en France et à l’étranger.<br />
Parallèlement, il collabore en tant que scénographe avec de nombreuses compagnies, dans des univers allant de la féerie baroque <i>La Belle et la Bête</i>, mise en scène par Cécile Roussat et Julien Lubek, à <i>Grand-peur et misère du troisième Reich</i> de Bertolt Brecht, mis en scène par Myriam Zwingel , <i>La femme comme champ de bataille</i> de Mattéi Visniec, <i>Top Girls</i> de Caryl Churchill, mis en scène par Aurélie Van Den Daele, en passant par une adaptation de <i>Huit femmes</i> mis en scène par Valérie Bral ou celle de La <i>Conversation de Bolzano</i> de Sandor Marai, mis en scène par Jean Louis Thamin.<br />
Il signe les décors des festivals comme <i>Les Marius</i>, cérémonie récompensant les meilleurs spectacles musicaux, ou encore pour le <i>Festival du Film Jules Verne</i> au Grand Rex, tous mis en scène par Samuel Sené. Il participe également à plusieurs productions audiovisuelles en tant que chef-décorateur, et crée les univers visuels de nombreux clips musicaux.<br />
Il travaille régulièrement avec le Studio-Théâtre d’Asnières où il scénographie plusieurs spectacles tels que <i>L’île des esclaves</i> et <i>Les acteurs de Bonne Foi</i> de Marivaux, mis en scène par Jean Louis Martin-Barbaz, <i>Un bon petit diable</i> d’après la Comtesse de Ségur, <i>Il n’y a plus d’après, il n’y a qu’aujourd’hui</i>, cabaret mis en scène par Yveline Hamon.<br />
Il crée également des masques et marionnettes pour le Shlemil Théâtre et pour le Théâtre de l’Étoile Bleue, notamment pour une adaptation de <i>L’arrache Coeur</i> de Boris Vian, mis en scène par Eric Bertrand.<br />
